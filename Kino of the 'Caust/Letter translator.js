// Function to translate the page
function translatePage(lang) {
    const translation = translations[lang] || translations['en']; // Fallback to English

    // Navigation
    document.querySelector('nav ul li a[href="About Kino of the \'Caust.html"]').innerHTML = translation.nav_about;
    document.querySelector('nav ul li a[href="The Archivist.html"]').innerHTML = translation.nav_founder;
    document.querySelector('nav ul li a[href="Movie categories.html"]').innerHTML = translation.nav_movies;
    document.querySelector('.dropdown a[href="login.html"]').innerHTML = translation.nav_login;
    document.querySelector('.dropdown a[href="sign up.html"]').innerHTML = translation.nav_signup;
    document.querySelector('.user-btn').innerHTML = `${translation.user_btn} <i class="fas fa-caret-down"></i>`;

    // Letter Content
    document.querySelector('.letter-title').innerHTML = translation.letter_title;
    document.querySelector('.site-title').innerHTML = translation.site_title;
    const scriptFontParas = document.querySelectorAll('.letter-content .script-font');
    scriptFontParas[0].innerHTML = translation.para_1;
    scriptFontParas[1].innerHTML = translation.para_2;
    scriptFontParas[2].innerHTML = translation.para_3;
    scriptFontParas[3].innerHTML = translation.para_4;
    scriptFontParas[4].innerHTML = translation.para_5;
    scriptFontParas[5].innerHTML = translation.para_6;
    scriptFontParas[6].innerHTML = translation.para_7;
    document.querySelector('.signature-line').innerHTML = translation.signature;
    document.querySelector('.archivist-link').innerHTML = translation.archivist_link;

    // Instructions Panel
    document.querySelector('.instructions-panel h2').innerHTML = translation.instructions_title;
    const instructions = document.querySelectorAll('.instructions-panel ul li');
    instructions[0].innerHTML = translation.instruction_1;
    instructions[1].innerHTML = translation.instruction_2;
    instructions[2].innerHTML = translation.instruction_3;
    instructions[3].innerHTML = translation.instruction_4;
    instructions[4].innerHTML = translation.instruction_5;
    instructions[5].innerHTML = translation.instruction_6;

    // Button
    document.querySelector('.button').innerHTML = translation.explore_button;

    // Footer
    document.querySelector('footer p').innerHTML = translation.footer_text;
}

// Initialize Google Translate (existing functionality)
function googleTranslateElementInit() {
    new google.translate.TranslateElement(
        { pageLanguage: 'en' },
        'google_translate_element'
    );
}

// Set default language and persist user preference
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'en';
    document.getElementById('languageSelect').value = savedLang;
    translatePage(savedLang);
});

// Save language preference when changed
document.getElementById('languageSelect').addEventListener('change', (e) => {
    localStorage.setItem('language', e.target.value);
    translatePage(e.target.value);
});